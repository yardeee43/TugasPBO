    public void tampilPU() {
        em = emf.createEntityManager();

        try {
            DefaultTableModel tbnmhs = new DefaultTableModel(new String[]{"NIM", "Nama", "Alamat", "Asal SMA", "Nama Orang Tua"}, 0);

            List<Mahasiswa> mahasiswaList = em.createNamedQuery("Mahasiswa.findAll", Mahasiswa.class).getResultList();

            for (Mahasiswa mk : mahasiswaList) {
                tbnmhs.addRow(new Object[]{
                    mk.getNim(),
                    mk.getNama(),
                    mk.getAlamat(),
                    mk.getAsalsma(),
                    mk.getNamaorangtua()
                });
            }

            table.setModel(tbnmhs);
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }

    public final void refreshModel() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(columns);
        for (String[] data : dataList) {
            model.addRow(data);
        }
        table.setModel(model);
    }

