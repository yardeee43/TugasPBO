    private void insertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertBtnActionPerformed
        String nim = nimTxt.getText();
        String nama = namaTxt.getText();
        String alamat = alamatTxt.getText();
        String asalsma = asalSma.getText();
        String namaortu = namaortuTXT.getText();
        if (nim.trim().isEmpty() && nama.trim().isEmpty() && alamat.trim().isEmpty() && asalsma.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan data anda terlebih dahulu");

        } else if (nim.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan nim terlebih dahulu");
        } else if (nama.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan nama terlebih dahulu");
        } else if (alamat.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan alamat terlebih dahulu");
        } else if (asalsma.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukkan asal sma terlebih dahulu");
        } else if (namaortu.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan nama orang tua terlebih dahulu");
        } else {
            em.getTransaction().begin();
            m.setNim(nimTxt.getText());
            m.setNama(namaTxt.getText());
            m.setAsalsma(asalSma.getText());
            m.setAlamat(alamatTxt.getText());
            m.setNamaorangtua(namaortuTXT.getText());
            em.persist(m);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "data berhasil ditambahkan");
            tampilPU();

            nimTxt.setText("");
            namaTxt.setText("");
            alamatTxt.setText("");
            asalSma.setText("");
            namaortuTXT.setText("");
            nimTxt.setEditable(true);
            updateBtn.setEnabled(false);

        }
    }//GEN-LAST:event_insertBtnActionPerformed

